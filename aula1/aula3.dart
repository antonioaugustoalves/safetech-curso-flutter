void main() {
  int a = 3;
  int b = 6;
  int c = 2;

  bool teste = a > b;
  print(teste);

  teste = a < b;
  print(teste);

  teste = a == b;
  print(teste);

  teste = a != b;
  print(teste);

  teste = a >= b;
  print(teste);

  teste = a <= b;
  print(teste);

  teste = (a > b) && (b > c);
  print(teste);

  teste = (a > b) || (b > c);
  print(teste);
}
